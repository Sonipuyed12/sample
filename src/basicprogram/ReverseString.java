package basicprogram;

public class ReverseString {
    public static void main(String[] args) {

        String str="sooonippuyedd";
        //reverse string
        for(int i=str.length()-1;i>=0;i--)
        {
            System.out.print(str.charAt(i));
        }

        //print count


    }
}
